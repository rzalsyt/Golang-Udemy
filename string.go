package main

import "fmt"

func main() {

	//function , kemampuan -> len("string") (mehitung jumlah karakter di stringnya)
	// -> "string"[number] mengambil karakter pada posisi yang di tentukan

	fmt.Println(len("Rizal Susmiyanto"))
	fmt.Println("Rizal Susmiyanto"[0]) //mengakhailkan byte
	fmt.Println("Rizal Susmiyanto"[1])
}
